void cell0(hls::stream<DTYPE> & in, hls::stream<DTYPE> & out)
{
	static DTYPE localCopy = 0;
	DTYPE in_copy = in.read();
	if(in_copy > localCopy) {
		out.write(localCopy);
		localCopy = in_copy;
	}
	else
	{
		out.write(in_copy);
	}
}

